// core/ipc: 相対パスの絶対パス正規化（クライアント側）。
// 要件3.2「相対パスは呼び出し元プロセスのカレントディレクトリ基準で解決する。起動中インスタンスへ
// 転送する場合は、クライアント側プロセスで絶対パスに正規化してから転送する（サーバー側の
// カレントディレクトリに依存させない）」。
//
// CWD はプラットフォーム層が取得して引数で渡す（純ロジック・gtest 検証）。Win32 の
// GetFullPathName を使わず、`.`/`..` の解決と区切り正規化をここで決定論的に行う
// （テスト可能性とサーバー非依存のため）。
#pragma once

#include <string>

namespace pika::core::ipc
{

// path を cwd（絶対パス前提）基準で絶対パスへ正規化する。
//   - path が既に絶対（ドライブ絶対 `C:\` / UNC `\\`）ならそのまま正規化（区切り統一・`..` 解決）
//   - ドライブ相対 `C:foo` は cwd と同じドライブのときのみ cwd 配下に解決する
//   - 相対パスは cwd に連結して解決する
//   - 区切りは Windows のバックスラッシュ '\\' に統一する
//   - `.` は除去、`..` は 1 つ上に畳む（ルートを超える `..` はルートで止める）
// 純粋に文字列処理で行い、FS にはアクセスしない（実在しないパスでも正規化する＝新規タブ用）。
std::string normalize_to_absolute(const std::string& path, const std::string& cwd);

} // namespace pika::core::ipc
