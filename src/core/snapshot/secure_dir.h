// core/snapshot: 本人のみアクセス可能な ACL でディレクトリを作成する。
// design.md 7章 / 要件9.1「snapshotsフォルダ・index・内容objectはユーザー本人のみアクセスできる
// 権限（ACL）で作成する」。機密ファイルのハッシュのみ記録と並ぶデータ最小化の一環。
#pragma once

#include <string_view>

namespace pika::core::snapshot
{

// path（UTF-8 絶対パス）のディレクトリを、現在のユーザー（所有者）のみがアクセスできる DACL で
// 作成する。親が無ければ親も作る。既存ディレクトリ（事前に弱権限で作られた可能性）にも owner-only
// DACL を冪等に強制適用して秘匿性を是正する（要件9.1）。
//
// 【戻り値の意味】「ディレクトリの作成可否」ではなく「owner-only DACL の強制適用に成功したか
// （＝秘匿性が担保されたか）」を表す。false でもディレクトリ自体は作成済みのことがあり（ACL のみ
// 失敗）、データ保全は継続してよい。**呼び出し側はこの戻り値を保全継続の中断条件に使ってはならない**
// （データ保全＞秘匿。退避＝最後の砦の作成を ACL 失敗で諦めない。設計原則1）。
// なお現状、作成自体の失敗（保全不能）と DACL 強制適用の失敗（保全可・秘匿のみ未達）はどちらも
// false に潰れている。失敗を区別して通知したい段になったら戻り値を拡張する。
bool create_secure_dir(std::string_view path);

} // namespace pika::core::snapshot
