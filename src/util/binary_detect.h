// util/binary_detect: テキスト/バイナリの素朴判定（wx 非依存・純ロジック）。
// 要件12.2（非テキスト＝「対応していない形式」表示）/ design.md 10章 B3 /
// CLAUDE.md「足さない・速く作る」。
//
// 画像でないファイルを開くとき、エディタへ流す前に内容先頭チャンク（呼び出し側で 8KB 程度）を見て
// 非テキスト（バイナリ）かを判定する。バイナリならエディタで文字化けさせず「対応していない形式」
// ビューへ誘導する（要件12.2）。完全な判定は不要で、誤判定してもデータは失わない（編集できないだけ）
// ため、コストの低い heuristic に留める（速く作る）:
//   1. NUL バイト（0x00）を含む → バイナリ（UTF-8/Shift_JIS テキストに NUL は出ない）。
//   2. 制御文字（タブ/改行/復帰を除く 0x00-0x1F と 0x7F）の比率が高い → バイナリ。
// UTF-16/UTF-8 BOM 付きテキストを誤判定しないよう、先頭 BOM はテキスト確定として扱う。
//
// 純ロジック（同一バイト列で同一出力）。空入力はテキスト扱い（空エディタで開く＝従来挙動）。
#pragma once

#include <string_view>

namespace pika::util
{

// chunk（ファイル先頭の一部で可）が非テキスト（バイナリ）らしいか判定する。
// true=バイナリ（「対応していない形式」へ誘導）/ false=テキスト（エディタで開く）。
// 判定基準は NUL 検出と制御文字比率（design B3 の素朴 heuristic）。
bool looks_binary(std::string_view chunk);

} // namespace pika::util
